export type Locale = "en" | "es";

export const locales = ["en", "es"] as const;

export function getLocale(value: string | string[] | undefined): Locale {
  const candidate = Array.isArray(value) ? value[0] : value;

  return candidate === "en" ? "en" : "es";
}

